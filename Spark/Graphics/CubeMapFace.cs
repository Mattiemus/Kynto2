namespace Spark.Graphics
{
    /// <summary>
    /// Enumeration of the faces of a CubeMap.
    /// </summary>
    public enum CubeMapFace
    {
        /// <summary>
        /// Face on the +X axis
        /// </summary>
        PositiveX = 0,

        /// <summary>
        /// Face on the -X axis
        /// </summary>
        NegativeX = 1,

        /// <summary>
        /// Face on the +Y axis
        /// </summary>
        PositiveY = 2,

        /// <summary>
        /// Face on the -Y axis
        /// </summary>
        NegativeY = 3,

        /// <summary>
        /// Face on the +Z axis
        /// </summary>
        PositiveZ = 4,

        /// <summary>
        /// Face on the -Z axis
        /// </summary>
        NegativeZ = 5
    }
}
