namespace Spark.Graphics
{
    /// <summary>
    /// Defines how primitives should be filled.
    /// </summary>
    public enum FillMode
    {
        /// <summary>
        /// The entire primitive should be drawn and filled.
        /// </summary>
        Solid = 0,

        /// <summary>
        /// Only the edges of primitives should be drawn.
        /// </summary>
        WireFrame = 1
    }
}
