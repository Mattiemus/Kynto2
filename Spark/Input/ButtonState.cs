namespace Spark.Input
{
    /// <summary>
    /// Enumeration defining the state of a button.
    /// </summary>
    public enum ButtonState
    {
        /// <summary>
        /// Button is currently released.
        /// </summary>
        Released = 0,

        /// <summary>
        /// Button is currently pressed.
        /// </summary>
        Pressed = 1
    }
}
