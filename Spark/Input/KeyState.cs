namespace Spark.Input
{
    /// <summary>
    /// Enumeration defining the state of a key.
    /// </summary>
    public enum KeyState
    {
        /// <summary>
        /// Key is currently up.
        /// </summary>
        Up = 0,

        /// <summary>
        /// Key is currently down.
        /// </summary>
        Down = 1
    }
}
