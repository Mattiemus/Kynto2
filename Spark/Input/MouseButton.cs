namespace Spark.Input
{
    /// <summary>
    /// Enumeration defining mouse buttons.
    /// </summary>
    public enum MouseButton
    {
        /// <summary>
        /// Left mouse button.
        /// </summary>
        Left = 0,

        /// <summary>
        /// Middle mouse button.
        /// </summary>
        Middle = 1,

        /// <summary>
        /// Right mouse button.
        /// </summary>
        Right = 2,

        /// <summary>
        /// XButton1
        /// </summary>
        XButton1 = 3,

        /// <summary>
        /// XButton2
        /// </summary>
        XButton2 = 4
    }
}
